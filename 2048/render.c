void render_tile(uint8_t x, uint8_t y, uint8_t num) {
    int i, j;

    MatrixColor colors[2] = {
        matrix_color(1, 0, 0),
        matrix_color(0, 0, 0),
    };

    colors[0] = matrix_color(num % 14, (14 - (num % 14)), (10 - (num % 10)));

    for (i = 0; i < 16; i++) {
        uint16_t row = tile_bitmaps[num - 1][i];
        for (j = 0; j < 16; j++) {
            uint16_t mask = 1 << (15 - j);
            int bit_value = (row & mask) ? 1 : 0;
            if (i % 15 == 0 && j % 15 == 0) {
                bit_value = 1;
            }
            matrix_draw_pixel(colors[bit_value], x + j, y + i);
        }
    }
}

void clear_tile(uint8_t x, uint8_t y) {
    int i, j;
    MatrixColor black = matrix_color(0, 0, 0);

    for (i = 0; i < 16; i++) {
        for (j = 0; j < 16; j++) {
            matrix_draw_pixel(black, x + j, y + i);
        }
    }
}

void render_board(GameState *gs) {
    int i, j;
    for (i = 0; i < 4; i++) {
        for (j = 0; j < 4; j++) {
            if (gs->board[i][j] == 0) {
                clear_tile(j * 16, i * 16);
            } else {
                render_tile(j * 16, i * 16, gs->board[i][j]);
            }
        }
    }

    matrix_swap_bufs();
}
